public class Employe {
    int id;
    String nom;
    String prenom;
    String email;
    String travail;

    public Employe(int id, String nom, String prenom, String email, String travail){
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.travail = travail;

    }
}
